def colorTest():
    for i in dir(Colors):
        if i[0:1] != "_" and i != "END":
            print("{:>16} {}".format(i, getattr(Colors, i) + i + Colors.END))

def loadQuotes():
    with open('assets/quotes.txt') as file:
        q = file.readlines()
